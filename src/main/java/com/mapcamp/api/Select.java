package com.mapcamp.api;

public class Select {
	private String shopname;
	private String address;
	
	public void setShopname(String name) {
		this.shopname = name;
	}
	public String getShopname() {
		return shopname;
	}
	
	public void setAddress(String addr) {
		this.address = addr;
	}
	public String getAddress() {
		return address;
	}
}
